package fun.deepsky.springboot.parallel;

public class Vehicle {

	private String vehicle_id;
	private String vehicle_name;
	private String search_code;
	private String brand_name_new;
	private String vehicle_maker;
	private double price_t;

	public String getVehicle_id() {
		return vehicle_id;
	}

	public void setVehicle_id(String vehicle_id) {
		this.vehicle_id = vehicle_id;
	}

	public String getVehicle_name() {
		return vehicle_name;
	}

	public void setVehicle_name(String vehicle_name) {
		this.vehicle_name = vehicle_name;
	}

	public String getSearch_code() {
		return search_code;
	}

	public void setSearch_code(String search_code) {
		this.search_code = search_code;
	}

	public String getBrand_name_new() {
		return brand_name_new;
	}

	public void setBrand_name_new(String brand_name_new) {
		this.brand_name_new = brand_name_new;
	}

	public String getVehicle_maker() {
		return vehicle_maker;
	}

	public void setVehicle_maker(String vehicle_maker) {
		this.vehicle_maker = vehicle_maker;
	}

	public double getPrice_t() {
		return price_t;
	}

	public void setPrice_t(double price_t) {
		this.price_t = price_t;
	}

}
